static int pCus_GetOrien(ss_cus_sensor *handle, CUS_CAMSENSOR_ORIT *orit)
{
    u16 data;

    SensorReg_Read(mirror_flip_reg[0].reg, (void *)&data);

    if ( (0x6 == (data & 0x6)) && (0x60 == (data & 0x60)) )
    {
        *orit = CUS_ORIT_M1F1;
    }
    else if ( (0x0 == (data & 0x6)) && (0x60 == (data & 0x60)) )
    {
        *orit = CUS_ORIT_M0F1;
    }
    else if ( (0x6 == (data & 0x6)) && (0x0 == (data & 0x60)) )
    {
        *orit = CUS_ORIT_M1F0;
    }
    else
    {
        *orit = CUS_ORIT_M0F0;
    }
    return SUCCESS;
}

