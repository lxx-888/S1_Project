static int pCus_GetFPS(ss_cus_sensor *handle)
{
    SC550XS_params *params  = (SC550XS_params *)handle->private_data;
    u32            max_fps = handle->video_res_supported.res[handle->video_res_supported.ulcur_res].u16max_fps;
    u32            tVts = ((params->tVts_reg[0].data & 0x7f) << 16) | (params->tVts_reg[1].data << 8) | (params->tVts_reg[2].data << 0);

    if (params->expo.fps >= 1000)
        params->expo.preview_fps = (params->expo.vts_standard * max_fps * 1000) / tVts;
    else
        params->expo.preview_fps = (params->expo.vts_standard * max_fps) / tVts;

    return params->expo.preview_fps;
}
static int pCus_SetFPS(ss_cus_sensor *handle, u32 fps)
{
    SC550XS_params *params  = (SC550XS_params *)handle->private_data;
    u32            max_fps = handle->video_res_supported.res[handle->video_res_supported.ulcur_res].u16max_fps;
    u32            min_fps = handle->video_res_supported.res[handle->video_res_supported.ulcur_res].u16min_fps;
   
    if (fps>=min_fps && fps<=max_fps)
    {
        params->expo.vts = (params->expo.vts_standard * (max_fps * 1000) + fps * 500) / (fps * 1000);
    }
    else if (fps>=(min_fps*1000) && fps<=(max_fps*1000))
    {
        params->expo.vts = (params->expo.vts_standard * (max_fps * 1000) + (fps >> 1)) / fps;
    }
    else
    {
        SENSOR_DMSG("[%s] FPS %d out of range.\n",__FUNCTION__,fps);
        return FAIL;
    }

    CamOsPrintf("=======================[%dx%d] fps %d, vts %d==============================\n",handle->video_res_supported.res[handle->video_res_supported.ulcur_res].u16width , handle->video_res_supported.res[handle->video_res_supported.ulcur_res].u16height,fps, params->expo.vts);

    params->expo.fps = fps;
    params->dirty = true;
    pCus_SetAEUSecs(handle, params->expo.expo_lef_us);
    return SUCCESS;
}

