//Get and check sensor ID
//if i2c error or sensor id does not match then return FAIL
static int pCus_GetSensorID(ss_cus_sensor *handle, u32 *id)
{
    int i,n;
    int table_length = ARRAY_SIZE(Sensor_id_table);
    I2C_ARRAY id_from_sensor[ARRAY_SIZE(Sensor_id_table)];

    for (n=0; n<table_length; ++n)
    {
        id_from_sensor[n].reg = Sensor_id_table[n].reg;
        id_from_sensor[n].data = 0;
    }

    *id = 0;
    if(table_length > 8)
        table_length = 8;

    SENSOR_DMSG("\n\n[%s]", __FUNCTION__);

    for (n=0; n<4; ++n) //retry , until I2C success
    {
        if (n>3)
          return FAIL;

        if (SensorRegArrayR((I2C_ARRAY*)id_from_sensor, table_length) == SUCCESS) //read sensor ID from I2C
          break;
        else
          SENSOR_MSLEEP(1);
    }

    //convert sensor id to u32 format
    for (i=0; i<table_length; ++i)
    {
       if (id_from_sensor[i].data != Sensor_id_table[i].data ){
        SENSOR_IMSG("[%s] Please Check SC550XS Sensor Insert!!\n", __FUNCTION__);
            return FAIL;
       }
       *id = ((*id) + id_from_sensor[i].data)<<8;
    }
    *id >>= 8;
    SENSOR_DMSG("[%s]SC550XS sensor, Read sensor id, get 0x%x Success\n", __FUNCTION__, (int)*id);
    return SUCCESS;
}

